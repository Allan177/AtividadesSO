// A fragmentação ocorre quando o espaço de armazenamento em um sistema é 
// utilizado de forma ineficiente, impactando negativamente o desempenho. 
// Existem dois principais tipos de fragmentação: interna e externa. 
// A fragmentação interna acontece quando um bloco de memória alocado é maior
//  do que o necessário para armazenar os dados, resultando em desperdício de 
//  espaço dentro do próprio bloco. Já a fragmentação externa ocorre quando 
//  a memória livre está dividida em pequenos blocos não contíguos, impossibilitando 
//  a alocação de processos maiores, mesmo que haja espaço total suficiente. 
//  Isso pode reduzir o desempenho do sistema ao exigir mais tempo para encontrar 
//  áreas adequadas para alocação, além de aumentar a necessidade de técnicas
//   como compactação ou realocação de memória para mitigar o problema.