// A alocação contígua armazena um processo em um único bloco contínuo de memória, 
// proporcionando acesso rápido e baixa sobrecarga, mas apresentando fragmentação 
// externa e dificuldade de expansão, como ocorre em partições fixas e variáveis.
//  Já a alocação não contígua divide o processo em blocos dispersos, oferecendo mais 
//  flexibilidade e eficiência, reduzindo a fragmentação externa e permitindo tamanhos
//   dinâmicos. No entanto, esse método aumenta a complexidade, torna o acesso mais lento
//    e exige tabelas de mapeamento para organização. Exemplos de técnicas utilizadas na 
//    alocação não contígua incluem a paginação e a segmentação.
